package com.seventh_root.atherna.stat;

public class AthernaStat {

    private int id;
    private String name;

    public AthernaStat(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

}
